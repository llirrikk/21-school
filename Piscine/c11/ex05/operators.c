int	plus(int x, int y)
{
	return (x + y);
}

int	minus(int x, int y)
{
	return (x - y);
}

int	division(int x, int y)
{
	return (x / y);
}

int	multiply(int x, int y)
{
	return (x * y);
}

int	mod(int x, int y)
{
	return (x % y);
}
