#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_is_negative(int n)
{
	char	res;

	if (n < 0)
		res = 'N';
	else
		res = 'P';
	ft_putchar(res);
}
