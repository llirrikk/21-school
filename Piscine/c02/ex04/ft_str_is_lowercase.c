int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	res;
	int	t;

	res = 1;
	i = 0;
	while (*(str + i) != '\0')
	{
		t = *(str + i);
		if (!(t >= 'a' && t <= 'z'))
		{
			res = 0;
			break ;
		}
		i++;
	}
	return (res);
}
